img = cv2.imread('../resources/graph.png')
graphBarImg = applyHSVFilter(img, 0, 0, 0, 255, 68, 126)
lineImg = applyHSVFilter(img, 51, 111, 135, 255, 105, 216)
imgStack = stackImages(1, ([img, graphBarImg, lineImg]))
cv2.imshow("Stacked Images", imgStack)
